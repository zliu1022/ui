#!/bin/bash

# 清空 out.txt 文件
> out.txt

cat <<EOF > out.txt
围棋死活做题界面程序如下，已经可以正常工作

python编程，在这个程序基础上进行改进，增加一个搜索题目的功能
1. 主界面，增加按钮“搜索”
2. 点击后打开一个空白的19x19棋盘，棋盘下面有2个按钮“搜索”，“取消”，提示：“左键黑子，右键白子”
3. 点击：
- 如果左键点击棋盘空白位置或者白子位置，则显示黑子；左键点击黑子位置，则设置为空白
- 如果右键点击棋盘空白位置或者黑子位置，则显示白子；右键点击白子位置，则设置为空白
- 点击“搜索”按钮后，把棋盘上的黑子、白子转换成 prepos 的格式如下，
  "prepos": {
    "b": [
      "ja",
      ...
      "sg"
    ],
    "w": [
      "ib",
      ...
      "rh"
    ]
  }
然后在 mongodb，db 库，q 表格进行搜索，q 表格文档中存在 prepos 字段
搜索失败，提示信息：“找不到”
搜索成功，提示信息：“找到”
- 点击“取消”，则还是显示原来的死活题目

不需要导出全部完整的代码，只需要指出修改哪个文件的哪个部分即可
EOF

# 遍历所有的 .py 文件
for file in *.py; do
    # 输出文件名
    echo "${file}：" >> out.txt

    # 输出文件内容
    echo '```python' >> out.txt
    cat "$file" >> out.txt
    echo '```' >> out.txt

    echo '' >> out.txt  # 添加空行分隔
done

